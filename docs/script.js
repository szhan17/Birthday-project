/* ============================================================
   SURPRISE MESSAGE — accordion reveal
   ============================================================ */
function revealSurprise() {
    const msg = $('#surpriseMessage');
    const btn = $('#surpriseBtn');
    state.surpriseOpen = !state.surpriseOpen;

    if (state.surpriseOpen) {
        msg.classList.add('open');
        btn.textContent = 'Hide Surprise 🙈';
        launchConfetti();
    } else {
        msg.classList.remove('open');
        btn.textContent = 'Click for a Surprise 🎁';
    }
}

/* ============================================================
   BACKGROUND MUSIC TOGGLE
   ============================================================ */
function initMusicToggle() {
    const btn   = $('#musicToggle');
    const audio = $('#bgMusic');

    btn.addEventListener('click', () => {
        // No audio source configured
        const hasSrc = audio.querySelector('source') || audio.src;
        if (!hasSrc || audio.src === window.location.href) {
            btn.title = '🎵 Add a .mp3 to /music/ and uncomment the <source> in index.html to enable music!';
            btn.textContent = '🎵';
            return;
        }

        if (state.musicPlaying) {
            audio.pause();
            btn.textContent = '🔇';
            btn.setAttribute('aria-label', 'Play music');
        } else {
            audio.play().catch(() => {});
            btn.textContent = '🎵';
            btn.setAttribute('aria-label', 'Pause music');
        }
        state.musicPlaying = !state.musicPlaying;
    });
}

