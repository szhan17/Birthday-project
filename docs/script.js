/* ============================================================
   IMAGE LOADING
   Strategy:
     1. Fetch images-manifest.json  (generated by generate-manifest.ps1)
     2. If unavailable, try loading images/1.jpg … images/50.jpg etc.
     3. If nothing found, show the setup instructions panel.
   ============================================================ */
async function loadImages() {
    try {
        const res = await fetch('images-manifest.json');
        if (!res.ok) throw new Error('manifest-missing');
        const data = await res.json();
        state.images = (data.images || []).filter(Boolean);
    } catch {
        // Manifest missing — attempt sequential detection
        state.images = await detectImagesSequential();
    }

    if (state.images.length === 0) {
        showNoImagesUI();
        return;
    }

    buildCarousel();
    buildGalleryGrid();
}

async function detectImagesSequential() {
    const exts = ['jpg', 'jpeg', 'png', 'webp', 'gif', 'avif'];
    const found = [];

    // Probe numbered filenames up to 60
    const probes = [];
    for (let n = 1; n <= 60; n++) {
        for (const ext of exts) {
            probes.push(`images/${n}.${ext}`);
        }
    }

    const results = await Promise.allSettled(
        probes.map(src => new Promise((resolve, reject) => {
            const img = new Image();
            img.onload  = () => resolve(src);
            img.onerror = () => reject();
            img.src = src;
        }))
    );

    results.forEach(r => { if (r.status === 'fulfilled') found.push(r.value); });
    return found;
}

function showNoImagesUI() {
    const noMsg = $('#noImagesMsg');
    const carWrap = $('.carousel-wrapper');
    if (noMsg)    noMsg.style.display = 'block';
    if (carWrap)  carWrap.style.display = 'none';
}

/* ============================================================
   CAROUSEL
   ============================================================ */
function buildCarousel() {
    const carousel    = $('#carousel');
    const dotsWrap    = $('#carouselDots');
    carousel.innerHTML = '';
    dotsWrap.innerHTML = '';

    state.images.forEach((src, i) => {
        // Slide
        const slide   = el('div', 'carousel-slide');
        const img     = el('img');
        img.src        = src;
        img.alt        = `Memory ${i + 1}`;
        img.loading    = 'lazy';
        const caption = el('div', 'carousel-caption');
        caption.textContent = CONFIG.captions[i % CONFIG.captions.length];
        slide.append(img, caption);
        carousel.appendChild(slide);

        // Dot
        const dot = el('button', 'dot' + (i === 0 ? ' active' : ''));
        dot.setAttribute('role', 'tab');
        dot.setAttribute('aria-label', `Photo ${i + 1}`);
        dot.onclick = () => goToSlide(i);
        dotsWrap.appendChild(dot);
    });

    updateCarousel();
    startAutoSlide();
}

function initCarouselControls() {
    $('#prevBtn').addEventListener('click', () => {
        state.currentSlide = (state.currentSlide - 1 + state.images.length) % state.images.length;
        updateCarousel();
        resetAutoSlide();
    });

    $('#nextBtn').addEventListener('click', () => {
        state.currentSlide = (state.currentSlide + 1) % state.images.length;
        updateCarousel();
        resetAutoSlide();
    });
}

function goToSlide(index) {
    state.currentSlide = index;
    updateCarousel();
    resetAutoSlide();
}

function updateCarousel() {
    const carousel = $('#carousel');
    if (!carousel) return;
    carousel.style.transform = `translateX(-${state.currentSlide * 100}%)`;

    $$('.dot').forEach((dot, i) => {
        dot.classList.toggle('active', i === state.currentSlide);
        dot.setAttribute('aria-selected', i === state.currentSlide);
    });
}

function startAutoSlide() {
    if (state.images.length < 2) return;
    state.slideTimer = setInterval(() => {
        state.currentSlide = (state.currentSlide + 1) % state.images.length;
        updateCarousel();
    }, CONFIG.slideInterval);
}

function resetAutoSlide() {
    clearInterval(state.slideTimer);
    startAutoSlide();
}

/* ============================================================
   GALLERY GRID
   ============================================================ */
function buildGalleryGrid() {
    const grid = $('#galleryGrid');
    grid.innerHTML = '';

    state.images.forEach((src, i) => {
        const item    = el('div', 'gallery-item');
        item.setAttribute('role', 'listitem');
        item.setAttribute('tabindex', '0');
        item.setAttribute('aria-label', `Open photo ${i + 1}`);

        const img     = el('img');
        img.src        = src;
        img.alt        = `Memory ${i + 1}`;
        img.loading    = 'lazy';

        const overlay = el('div', 'gallery-overlay', '<span>🔍</span>');

        item.append(img, overlay);

        item.addEventListener('click',   () => openLightbox(i));
        item.addEventListener('keydown', (e) => { if (e.key === 'Enter' || e.key === ' ') openLightbox(i); });

        // Staggered entrance animation
        item.style.opacity   = '0';
        item.style.transform = 'scale(0.82)';
        item.style.transition = `opacity 0.45s ease ${i * 65}ms, transform 0.45s ease ${i * 65}ms`;

        grid.appendChild(item);

        // Trigger entrance via rAF to allow transition
        requestAnimationFrame(() => {
            requestAnimationFrame(() => {
                item.style.opacity   = '1';
                item.style.transform = 'scale(1)';
            });
        });
    });
}

/* ============================================================
   LIGHTBOX
   ============================================================ */
function initLightboxControls() {
    const lb      = $('#lightbox');
    const overlay = $('#lightboxOverlay');
    const closeBtn = $('#lightboxClose');
    const prevBtn  = $('#lbPrev');
    const nextBtn  = $('#lbNext');

    overlay.addEventListener('click', closeLightbox);
    closeBtn.addEventListener('click', closeLightbox);

    prevBtn.addEventListener('click', () => {
        state.lightboxIndex = (state.lightboxIndex - 1 + state.images.length) % state.images.length;
        updateLightbox();
    });

    nextBtn.addEventListener('click', () => {
        state.lightboxIndex = (state.lightboxIndex + 1) % state.images.length;
        updateLightbox();
    });

    // Keyboard nav
    document.addEventListener('keydown', (e) => {
        if (!lb.classList.contains('active')) return;
        switch (e.key) {
            case 'Escape':      closeLightbox(); break;
            case 'ArrowLeft':
                state.lightboxIndex = (state.lightboxIndex - 1 + state.images.length) % state.images.length;
                updateLightbox(); break;
            case 'ArrowRight':
                state.lightboxIndex = (state.lightboxIndex + 1) % state.images.length;
                updateLightbox(); break;
        }
    });

    // Touch swipe support on lightbox
    let touchStartX = 0;
    lb.addEventListener('touchstart', (e) => { touchStartX = e.changedTouches[0].clientX; }, { passive: true });
    lb.addEventListener('touchend',   (e) => {
        const dx = e.changedTouches[0].clientX - touchStartX;
        if (Math.abs(dx) < 40) return;
        if (dx < 0) {
            state.lightboxIndex = (state.lightboxIndex + 1) % state.images.length;
        } else {
            state.lightboxIndex = (state.lightboxIndex - 1 + state.images.length) % state.images.length;
        }
        updateLightbox();
    }, { passive: true });
}

function openLightbox(index) {
    if (state.images.length === 0) return;
    state.lightboxIndex = index;
    const lb = $('#lightbox');
    lb.style.display = 'flex';           // Ensure it's flex before adding class
    requestAnimationFrame(() => lb.classList.add('active'));
    updateLightbox();
    document.body.style.overflow = 'hidden';
    $('#lightboxClose').focus();
}

function updateLightbox() {
    const img     = $('#lightboxImg');
    const caption = $('#lightboxCaption');
    img.style.opacity = '0';
    setTimeout(() => {
        img.src = state.images[state.lightboxIndex];
        img.alt = `Memory ${state.lightboxIndex + 1}`;
        img.style.opacity = '1';
    }, 120);
    caption.textContent = CONFIG.captions[state.lightboxIndex % CONFIG.captions.length];
}

function closeLightbox() {
    const lb = $('#lightbox');
    lb.classList.remove('active');
    setTimeout(() => { lb.style.display = 'none'; }, 300);
    document.body.style.overflow = '';
}

/* ============================================================
   CONFETTI — Canvas-based, no external lib
   ============================================================ */
function launchConfetti() {
    const canvas = $('#confettiCanvas');
    const ctx    = canvas.getContext('2d');
    canvas.width  = window.innerWidth;
    canvas.height = window.innerHeight;

    const palette = ['#FFD93B', '#3B6BB5', '#FF6B6B', '#51CF66', '#FF9F43', '#ffffff', '#1C3F6E', '#FF8ED4'];

    state.confettiParts = Array.from({ length: CONFIG.confettiCount }, () => ({
        x:        Math.random() * canvas.width,
        y:        -10 - Math.random() * canvas.height * 0.6,
        size:     5 + Math.random() * 9,
        color:    palette[Math.floor(Math.random() * palette.length)],
        shape:    Math.random() < 0.15 ? 'banana' : (Math.random() < 0.5 ? 'circle' : 'rect'),
        vx:       (Math.random() - 0.5) * 3.5,
        vy:       2.2 + Math.random() * 4,
        rot:      Math.random() * 360,
        rotV:     (Math.random() - 0.5) * 6,
        opacity:  1,
    }));

    if (state.rafId) cancelAnimationFrame(state.rafId);
    drawConfetti(ctx, canvas);
}

function drawConfetti(ctx, canvas) {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    state.confettiParts = state.confettiParts.filter(p => p.opacity > 0.01);

    state.confettiParts.forEach(p => {
        p.x   += p.vx;
        p.y   += p.vy;
        p.rot += p.rotV;
        if (p.y > canvas.height - 60) p.opacity -= 0.025;

        ctx.save();
        ctx.globalAlpha = p.opacity;
        ctx.translate(p.x, p.y);
        ctx.rotate(p.rot * Math.PI / 180);
        ctx.fillStyle = p.color;

        if (p.shape === 'circle') {
            ctx.beginPath();
            ctx.arc(0, 0, p.size / 2, 0, Math.PI * 2);
            ctx.fill();
        } else if (p.shape === 'banana') {
            ctx.font = `${p.size * 1.8}px serif`;
            ctx.textAlign = 'center';
            ctx.textBaseline = 'middle';
            ctx.fillText('🍌', 0, 0);
        } else {
            ctx.fillRect(-p.size / 2, -p.size / 2, p.size, p.size * 0.55);
        }
        ctx.restore();
    });

    if (state.confettiParts.length > 0) {
        state.rafId = requestAnimationFrame(() => drawConfetti(ctx, canvas));
    }
}

window.addEventListener('resize', () => {
    const canvas = $('#confettiCanvas');
    canvas.width  = window.innerWidth;
    canvas.height = window.innerHeight;
}, { passive: true });

/* ============================================================
   TYPEWRITER EFFECT
   ============================================================ */
function startTypewriter() {
    if (state.typewriterDone) return;
    state.typewriterDone = true;

    const target  = $('#typewriterText');
    const cursor  = $('.cursor');
    const text    = CONFIG.typewriterMessage;
    let index     = 0;
    target.textContent = '';

    function type() {
        if (index < text.length) {
            target.textContent += text[index];
            index++;
            setTimeout(type, CONFIG.typewriterSpeed);
        } else {
            // Hide blinking cursor when done
            if (cursor) setTimeout(() => cursor.style.display = 'none', 800);
        }
    }
    type();
}

/* ============================================================
   SCROLL-TRIGGERED ANIMATIONS (IntersectionObserver)
   ============================================================ */
function initScrollAnimations() {
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (!entry.isIntersecting) return;

            const el = entry.target;

            // Start typewriter when message section enters view
            if (el.id === 'message') {
                setTimeout(startTypewriter, 500);
            }

            // Timeline items
            if (el.classList.contains('timeline-item')) {
                el.classList.add('visible');
            }

            // Quote cards — staggered reveal
            if (el.classList.contains('fade-card')) {
                el.classList.add('visible');
            }
        });
    }, { threshold: 0.18 });

    $('#message') && observer.observe($('#message'));
    $$('.timeline-item').forEach(item => observer.observe(item));
    $$('.fade-card').forEach(card => observer.observe(card));
}

